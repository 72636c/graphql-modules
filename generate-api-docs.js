'use strict';
const jsdoc2md = require('jsdoc-to-markdown');
const fs = require('fs');
const path = require('path');

/* input and output paths */
const inputFile = './packages/**/src/**/*.ts';
const outputDir = path.join(__dirname, './docs/api/');

/* get template data */
const templateData = jsdoc2md.getTemplateDataSync({ files: inputFile });

/* reduce templateData to an array of class names */
const classNames = templateData.reduce((classNames, identifier) => {
  if (identifier.kind === 'class') classNames.push(identifier.name);
  return classNames;
}, []);

/* create a documentation file for each class */
for (const className of classNames) {
  const splittedPath = templateData[0].meta.path.split('/');
  const packageName = splittedPath[splittedPath.findIndex(a => a === 'packages') + 1];
  const classId = className.toLowerCase();
  const template = `---
id: ${packageName}-${classId}
title: ${className}
sidebar_label: ${className}
---

<!-- DO NOT EDIT THIS FILE! IT'S GENERATED BY JSDOC2MD!!! -->

{{#class name="${className}"}}{{>docs}}{{/class}}
`;
  const output = jsdoc2md.renderSync({ data: templateData, template: template });
  fs.writeFileSync(path.resolve(outputDir, packageName, `${classId}.md`), output);
}
